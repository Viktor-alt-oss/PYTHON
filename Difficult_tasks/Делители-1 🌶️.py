# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑ –æ—Ç—Ä–µ–∑–∫–∞ [ùëé;ùëè][a;b] (–æ—Ç ùëé –¥–æ b –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ) —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π.
# –ï—Å–ª–∏ —á–∏—Å–µ–ª —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –∏—Å–∫–æ–º—ã–º —á–∏—Å–ª–æ–º —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∏–∑ –Ω–∏—Ö. 
# –í–∞—à–∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–æ–¥–∏—Ç—å –æ—Ç–≤–µ—Ç –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ñ–æ—Ä–º–∞—Ç–µ:

a, b = int(input()), int(input())
largest = 0
counter = 0
for i in range(a, b + 1):
    total = 0
    for j in range(1, i + 1):
        if i % j == 0:
            total += j
            if total >= counter and i >= largest:
                counter = total
                largest = i
print(largest, counter)
